using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;
using System.Threading.Tasks;

namespace LibraryManager.Utils
{
    public static class ValidateHelper
    {
        public static bool validateInteger(String num)
        {
            String regex = @"^[0-9]+$";
            return Regex.IsMatch(num, regex);
        }
        public static bool validateVietnamese(String s)
        {
            String regex = @"^[a-zA-ZÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚĂĐĨŨƠàáâãèéêìíòóôõùúăđĩũơƯĂẠẢẤẦẨẪẬẮẰẲẴẶẸẺẼỀỀỂẾưăạảấầẩẫậắằẳẵặẹẻẽềềểếỄỆỈỊỌỎỐỒỔỖỘỚỜỞỠỢỤỦỨỪễệỉịọỏốồổỗộớờởỡợụủứừỬỮỰỲỴÝỶỸửữựỳỵỷỹ\s]+$";
            return Regex.IsMatch(s, regex);
        }
        public static bool validateDecimal(String num)
        {
            String regex = @"^[0-9\s]+(\.)?[0-9]*$";
            return Regex.IsMatch(num, regex);
        }
        public static bool validateVietnameseWithNum(String s)
        {
            String regex = @"^[a-zA-Z0-9ÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚĂĐĨŨƠàáâãèéêìíòóôõùúăđĩũơƯĂẠẢẤẦẨẪẬẮẰẲẴẶẸẺẼỀỀỂưăạảấầẩẫậắằẳẵặẹẻẽềềểỄỆỈỊỌỎỐỒỔỖỘỚỜỞỠỢỤỦỨỪễệỉịọỏốồổỗộớờởỡợụủứừỬỮỰỲỴÝỶỸửữựỳỵỷỹ\s]+$";
            return Regex.IsMatch(s, regex);
        }
    }
}
